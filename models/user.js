const mongoose = require('mongoose');
let Schema = mongoose.Schema

const userSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    },
});

USER = mongoose.model('user', userSchema);
module.exports = USER